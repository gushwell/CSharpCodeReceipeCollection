using System;

{
    //条件分岐処理を書く(if, else無し)
    var number = 10;
    if (number > 0)
        Console.WriteLine("numberは正の数です");
}
{
    // 条件分岐処理を書く(if, else-if)
    var number = 1;
    if (number == 0)
        Console.WriteLine("numberは0です");
    else
        Console.WriteLine("numberは0以外です");
}

