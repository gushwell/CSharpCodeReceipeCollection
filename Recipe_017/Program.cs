using System;

var num = 10;
Console.Write($"{++num}");
Console.WriteLine($" {num}");
num = 10;
Console.Write($"{num++}");
Console.WriteLine($" {num}");
num = 10;
Console.Write($"{--num}");
Console.WriteLine($" {num}");
num = 10;
Console.Write($"{num--}");
Console.WriteLine($" {num}");

